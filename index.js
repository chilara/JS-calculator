const num1 = prompt("enter first number")
const opr = prompt("enter mathematical operator")
const num2 = prompt("enter second number")

if(opr == "+")

{
    alert(Number(num1) + Number(num2))
}
else if(opr == "-")
{
    alert(Number(num1) - Number(num2))
}
else if(opr == "*")
{
    alert(Number(num1) * Number(num2))
}
else if(opr == "/")
{
    alert(Number(num1) / Number(num2))
} 